      <h3>2.WORTEL</h3>
      <p  class="justify">Wortel merupakan sayuran berwarna oranye yang banyak digemari, 
      karena rasanya yang enak dan manfaat wortel yang melimpah. Wortel bisa dimakan mentah, direbus, atau digoreng, 
      dibuat jus, atau campuran puding.</p>
      <p  class="justify">Manfaat wortel didapat dari kandungan beta-karoten di dalamnya,
       yang berguna sebagai antioksidan. Selain beta-karoten, manfaat wortel juga didapat dari kandungan serat, 
       vitamin K, dan kalium. Wortel mengandung serat makanan yang bisa memperbaiki kondisi saluran pencernaan yang 
       terganggu seperti diare atau konstipasi.</p>
